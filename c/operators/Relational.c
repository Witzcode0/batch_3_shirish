#include<stdio.h>

void main(){
    int num1 = 10, num2 = 20;

    // 1 - true
    // 0 - false
    printf("==: %d\n", num1==num2); // equal
    printf("!=: %d\n", num1!=num2); // not equal
    printf("<: %d\n", num1<num2); // less then
    printf("<=: %d\n", num1<=num2); // less then or equal
    printf(">: %d\n", num1>num2); // grater then
    printf(">= %d\n", num1>=num2); // greter then or equal
}
